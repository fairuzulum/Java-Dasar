public class Aritmatika {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        // Penjumlahan
        System.out.println(a+b);

        // Pengurangan
        System.out.println(a-b);

        // Perkalian
        System.out.println(a*b);

        // Pembagian
        System.out.println(a/b);

        // Modulus
        System.out.println(a % b);
    }
}
