public class OperatorAssigment {
    public static void main(String[] args) {
        //Assignment Operator Penjumlahan
        int a = 5;
        a += 5;
        System.out.println("Nilai a = " + a);
        //Assignment Operator Pengurangan
        int b = 5;
        b -= 3;
        System.out.println("Nila b = " + b);
        //Assigment Operator Perkalian
        int c = 5;
        c *= 3;
        System.out.println("Nilai c = " + c);
        //Assignment Operator Pembagian
        int d = 100;
        d /= 10;
        System.out.println("Nilai d = " + d);
        //Assignment Operator Modulus
        int e = 5;
        e %= 3;
        System.out.println("Nilai e = " + e);

    }
}
